// operator komparasi
// == equality
// != inequality
// === strict equality
// !== strict inequality
// > lebih besar dari
// >= lebih besar sama dengan
// < lebih kecil
// <= lebih kecil sama dengan

console.log(`15 > 7 = ${15 > 7}`);
let dataBool = 15 > 7;
console.log(`15 > 7 = ${dataBool}`);

console.log(`12 >= 12 = ${12 >= 12}`);
console.log(`7 < 15 = ${7 < 15}`);
console.log(`7 <= 7 = ${7 <= 7}`);

//persamaan
console.log(`3 == "3" = ${3 == "3"}`);
console.log(`3 != 3 = ${3 != "3"}`);

console.log(`3 === 3 = ${3 === 3}`);
console.log(`3 === "3" = ${3 === "3"}`);
console.log(`"3" === "3" = ${"3" === "3"}`);

console.log(`3 !== 3 = ${3 !== 3}`);
console.log(`3 !== "3" = ${3 !== "3"}`);
console.log(`"3" !== "3" = ${"3" !== "3"}`);

